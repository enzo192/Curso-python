
numeros = [4,7,1,42,15]


#Encontrando el numero mayor de una lista
numero_mas_alto = max(numeros)
print(numero_mas_alto)

#Encontrando el numero menbor de una lista
numero_mas_bajo = min(numeros)
print(numero_mas_bajo)

#redondeando a 6 decimales
numero = round(12.345676,3)
print(numero)

#retorna False -> 0, vacio, False, ninguno
resultado_bool = bool(0)
print(resultado_bool)

#retornal True, si todos los valores son verdaderos
resultado_all = all([0,True])
print(resultado_all)

#suma todos los numeros
suma_total = sum(numeros)
print(suma_total)